export default function Letter({
  letter,
  isHighlighted,
  onHover,
  onToggleStar,
}) {
    //console.log(`Letter ${letter.id}: isHighlighted =`, isHighlighted);
  return (
    <li
      className={
        isHighlighted ? 'highlighted' : ''
      }
      onFocus={() => {
        onHover(letter);        
      }}
      onPointerMove={() => {
        onHover(letter);
      }}
    >
      <button onClick={() => {
        onToggleStar(letter);
      }}>
        {letter.isStarred ? 'Unstar' : 'Star'}
      </button>
      {letter.subject}
    </li>
  )
}
